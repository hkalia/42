#include <string.h>
#include <stdlib.h>

char	*ft_strnew(size_t len)
{
	size_t	i;
	char	*new;

	new = (char *)malloc(sizeof(char) * (len + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (i < len)
	{
		new[i] = '\0';
		i++;
	}
	new[i] = '\0';
	return (new);
}
