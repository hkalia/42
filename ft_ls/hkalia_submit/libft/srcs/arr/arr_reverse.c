#include <arr.h>

void	arr_reverse(t_arr *src)
{
	size_t	i;
	size_t	j;

	if (src == 0)
		return ;
	i = 0;
	j = src->len - 1;
	while (j > i)
	{
		arr_swap(src, i, j);
		++i;
		--j;
	}
}
