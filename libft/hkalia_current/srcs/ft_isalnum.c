#include <ft_ctype.h>

int		ft_isalnum(int src)
{
	if ((src >= '0' && src <= '9') || (src >= 'A' && src <= 'Z')
			|| (src >= 'a' && src <= 'z'))
		return (1);
	return (0);
}
