#include <string.h>
#include <stdlib.h>

void	ft_tbldel(char ***tbl)
{
	size_t	i;

	i = 0;
	while (*tbl[i])
	{
		free(*tbl[i]);
		i++;
	}
	*tbl = NULL;
}
